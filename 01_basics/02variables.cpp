#include <iostream>
using namespace std;
// variables

int main(){
	
	// int (whole number)
	int num1 = 5;
	int num2 = 10;
	int sum = num1 + num2;
	cout << sum << endl;
	
	// double (decimal number)
	double z = 10.5;
	cout << z << endl;
	
	// char (single character)
	char grade = 'A'; // single quote only
	cout << grade << endl;
	
	// bool (true or false)
	bool student = true;
	
	// string (objects that represent a sequence of text)
	string name = "Dev";
	std::string age = "18";
	cout << name << endl;
	cout << age << endl;
	cout << "Hello I am " << name << " and I am " << age << " yrs old";
	
	return 0;
}
